    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel tb1mode = (DefaultTableModel) jTable2.getModel();
        tb1mode.setRowCount(0);//delete all rows in jtable for new data
        try {
            bHandler.connect();//connect to database
        } catch (IOException ex) {
            Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ArrayList<Bill> bills;
        try {
            bills = bHandler.getListOfBills();//get all bills
            for (Bill b : bills) {//for all this restraunt bills
                if (b.getResId() == LoginForm.res.getId()) {//check restraunt id
                    String tbData[] = {String.valueOf(b.getId()), String.valueOf(b.getClientId()), String.valueOf(b.getFoodsId()), b.getFoodsName(), String.valueOf(b.getCost())};
                    tb1mode.addRow(tbData);//add to jtable-> | Bill id | Client Id | Foods Id | Foods Name | cost |
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManagerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel tb1mode = (DefaultTableModel) jTable3.getModel();
        tb1mode.setRowCount(0);//delete all jtable rows for new data

        try {
            bHandler.connect();

        } catch (IOException ex) {
            Logger.getLogger(ManagerForm.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
        int mId = LoginForm.res.getMenuId();//get menu id of this restaurant manager

        ArrayList<Food> foods;
        try {
            foods = bHandler.getListOfFoods();//get all foods
            for (Food food : foods) {//for all this menu foods
                if (food.getMenuId() == mId) {//check menu id
                    String tbData[] = {String.valueOf(food.getId()), food.getName(), String.valueOf(food.getCost()), String.valueOf(food.getCapacity())};
                    tb1mode.addRow(tbData);//add to jtable-> | food id | food name | food cost | food capacity |
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(ManagerForm.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed
