    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int x = 0;
        Food food = new Food();
        food.setId(Integer.parseInt(fuptxt.getText()));
        if (!(fpuptxt.getText().equals(""))) {//food price update
            food.setCost(Integer.parseInt(fpuptxt.getText()));
            x += 1;
        }
        if (!(fcuptxt.getText().equals(""))) {//food capacity update
            food.setCapacity(Integer.parseInt(fcuptxt.getText()));
            x += 2;
        }
        try {
            bHandler.editFood(food, x);

        } catch (SQLException ex) {
            Logger.getLogger(ManagerForm.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "successfully updated", "", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

