    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DBHandler db = new DBHandler();
        try {
            db.connect();
        } catch (IOException ex) {
            Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        int resId = id(), menuId = id(), managerid = id();

        Manager manager = new Manager(resId, menuId, managerid, ustxt.getText(), pstxt.getText());//creat new manager
        db.addManager(manager);

        Restaurant res = new Restaurant(resId, rntxt.getText(), menuId, managerid);//creat new restaurant
        db.addRestraunt(res);

        Menu menu = new Menu(menuId);//creat new menu
        db.addMenu(menu);

        JOptionPane.showMessageDialog(null, "successfully registered! your id is :" + managerid, "Registered", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

    public int id() {//random id
        Random rn = new Random();
        int id = rn.nextInt();
        while (id < 0) {
            id = rn.nextInt();
        }
        return id;
    }

